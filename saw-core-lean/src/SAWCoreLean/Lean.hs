module SAWCoreLean.Lean
  ( TranslationConfiguration(..)
  , preamble
  , translateTermAsDeclImports
  , ppTranslationError
  ) where

import           Prettyprinter

import qualified Language.Lean.AST        as Lean
import           SAWCore.SharedTerm

import           SAWCoreLean.Monad
import qualified SAWCoreLean.Term         as TermTranslation

-- | Imports emitted at the top of every generated file.
--
-- Phase 1: the @CryptolToLean@ root module (under
-- @saw-core-lean/lean/@) re-exports the three handwritten support
-- modules. The @open@s let generated code refer to primitives
-- unqualified (@Bit@, @Vec@, @bitvector@, …).
--
-- Generated preludes (@SAWCorePrelude@,
-- @CryptolPrimitivesForSAWCore@) will be added in Phase 2.
preamble :: TranslationConfiguration -> Doc ann
preamble _configuration = vsep
  [ "/- Mandatory imports from saw-core-lean -/"
  , "import CryptolToLean"
  , ""
  , "open CryptolToLean.SAWCoreScaffolding"
  , "open CryptolToLean.SAWCoreVectors"
  , "open CryptolToLean.SAWCoreBitvectors"
  , ""
  , "/- Code generated by saw-core-lean -/"
  ]

-- | Translate a single SAWCore 'Term' (with its type) to a Lean @def@
-- with the given name, prepended by the standard preamble.
translateTermAsDeclImports ::
  TranslationConfiguration -> Lean.Ident -> Term -> Term ->
  Either TranslationError (Doc ann)
translateTermAsDeclImports configuration name t tp = do
  doc <- TermTranslation.translateDefDoc configuration name t tp
  pure (vcat [preamble configuration, hardline <> doc])
